#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("Operadores de Asignacion");

    int valeu;

    valeu = 11;

    printf("El valor de nuestra valriables es: %i \n", valeu);

    valeu+= 1;

    printf("El valor de nuestra valriables es: %i \n", valeu);

    valeu -= 2;

    printf("El valor de nuestra valriables es: %i \n", valeu);

    valeu *= 5;

    printf("El valor de nuestra valriables es: %i \n", valeu);

    valeu /= 4;

    printf("El valor de nuestra valriables es: %i \n", valeu);

    valeu %= 6;

    printf("El valor de nuestra valriables es: %i \n", valeu);

    return 0;
}
