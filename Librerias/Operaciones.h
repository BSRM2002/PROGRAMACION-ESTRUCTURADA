
float addition(float valueA, float valueB)
{
    return valueA + valueB;
}

float substraction(float valueA, float valueB)
{
    return valueA - valueB;
}

float multication(float valueA, float valueB)
{
    return valueA * valueB;
}

float division (float valueA, float valueB)
{
    return valueA / valueB;
}
